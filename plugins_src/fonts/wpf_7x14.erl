-module(wpf_7x14).
-export([desc/0,width_fun/0,width/0,height/0,draw/1,char/1,bold/1]).

desc() ->
    "Large (7x14)".

width_fun() -> fun cw/1.

width() -> 7.
height() -> 14.

draw([C|T]) ->
    char(C),
    draw(T);
draw([]) -> ok.

bold([C|T]) ->
    bold_char(C),
    bold(T);
bold([]) -> ok.

cw(32) -> 5;
cw($,) -> 4;
cw($.) -> 5;
cw($:) -> 5;
cw($-) -> 5;
cw($i) -> 2;
cw($l) -> 2;
cw($m) -> 10;
cw($r) -> 6;
cw($t) -> 5;
cw($I) -> 4;
cw(160) -> 3;
cw(169) -> 9;
cw(_) -> 7.

bold_char(C) ->
    char(C),
    Cw = cw(C),
    gl:bitmap(1, 1, 0, 0, -Cw+1, 0, <<0>>),
    char(C).

